// step 1 send otp 

const User = require("../models/User");
const otpGenerate = require("../utils/otpGenerator");
const response = require("../utils/responseHandler");
const tiwilloService = require('../services/twilloService');
const generateToken = require("../utils/generateToken");
const sentOtpToEmail = require("../services/emailService");
const { uploadFileToCloudinary } = require("../config/cloudinaryConfig");
const Conversation = require('../models/Conversation')

const sentOtp = async(req,res) =>{
  const {phoneNumber, phoneSuffix,email} = req.body;
  const otp = otpGenerate();
  const expiry  = new Date(Date.now()+5 * 60 *1000)
  try {
    let user;
    if(email){
      user = await User.findOne({email})
      if(!user){
        user =new User({email})
      }
      user.emailOtp = otp;
      user.emailOtpExpiry = expiry;
      await user.save()

       await sentOtpToEmail(email,otp)
      return response(res,200,'Otp sent on email', {email})
    }
    if(!phoneNumber || !phoneSuffix){
      return response(res,400,"Phone number and phone suffix needed !!")
    }

    const fullPhoneNumber = `${phoneSuffix}${phoneNumber}`
    user = await User.findOne({phoneNumber});
    if(!user){
      user = await new User({phoneNumber,phoneSuffix})
    }
    await tiwilloService.sendOtpToPhoneNumber(fullPhoneNumber)
    await user.save();
    
    return response(res,200,"Otp send Successfully", user)
  } catch (error) {
    console.log(error)
    return response(res,500,"Internal server Error !!")
  }
}

//step - 2 Verify otp 

const verifyOtp = async(req,res)=>{
  const  {phoneNumber, phoneSuffix,email,emailOtp} = req.body
  try {
    let user;
    if(email){
      user = await User.findOne({email})
      if(!user){
        return response (res,404,`User not Found`)
      }
      const now = new Date();
      if(!user.emailOtp || String(user.emailOtp) !==String(emailOtp) || now > new Date(user.emailOtpExpiry)){
        return response(res,400,'Invalid or Expired otp')
      }
       
      user.isVerified = true;
      user.emailOtp=emailOtp;
      user.emailOtpExpiry = null;
      await user.save();
    }
    else{
       if(!phoneNumber || !phoneSuffix){
        //  console.log(error)
      return response(res,400,"Phone number and phone suffix needed !!")
    }
     const fullPhoneNumber = `${phoneSuffix}${phoneNumber}`;
     user =  await User.findOne({phoneNumber});
     if(!user){
      return response(res,404,'User not found')
     }
     const result = await tiwilloService.verifyOtp(fullPhoneNumber,otp);
     if(result.status !== 'approved'){
      return response(res,400,'Invalid Otp')
     }
     user.isVerified = true;
     await user.save();
    }
    const token = generateToken(user?._id);
    res.cookie('auth_token',token, {
      httpOnly:true,
      maxAge:1000*60 *60 *24 *365
    })
    return response(res,200,'otp verified successfully',{token , user})
  } 
  catch (error) {
    console.log(error)
    return response(res,500,"Internal server Error !!")
  }
}
// update profile 

const updateProfile = async(req,res)=>{
  const {username,agreed,about} = req.body;
  const userId = req.user.userId;
  try {
    const user  = await User.findById(userId);
    const file = req.file;
    if(file){
      const uploadResult = await uploadFileToCloudinary(file)
      console.log(uploadResult)
      user.profilePicture = uploadResult?.secure_url;
    }else if(req.body.profilePicture){
      user.profilePicture= req.body.profilePicture;
    }

    if(username) user.username = username;
    if(agreed) user.agreed = agreed;
    if(about) user.about = about;
    await user.save();

    return response(res,200,'user profile updated successfully',user)
  } catch (error) {
    console.error(error);
    return response(res,500,"Internal server error")
  }
}


// check authentication 
const checkAuthenticate = async (req,res)=>{
  try {
    const userId = req.user.userId;
    if(!userId){
      return response(res,404,'unauthorized ! please login before access our app..')
    }
    const user = await User.findById(userId);
    if(!user){
      return response(res,404,'User not found')
    }
    return response(res,200,'user retrived and allow to use whatsapp',user)
  } catch (error) {
    console.error(error);
    return response(res,500,"Internal server error")
  }
}

const logout = (req,res)=>{
  try {
    res.cookie('auth_token',"",{expires:new Date(0)})
    return response(res,200,'user logout successfully')
  } catch (error) {
       console.error(error);
    return response(res,500,"Internal server error")
  }
}

const getAllUsers = async (req,res)=>{
  const loggedInUser = req.user.userId;
  try {
    const users = await User.find({_id:{$ne:loggedInUser}}).select("username profilePicture lastSeen isOnline about phoneNumber phoneSuffix ").lean();

   const userWithConversation = await Promise.all(
    users.map(async (user)=>{
      const conversation = await Conversation.findOne({
        participants:{$all :[loggedInUser,user?._id]}
      }).populate({
        path:'lastMessage',
        select:"content createdAt sender receiver"
      }).lean()

      return {
        ...user,
        conversation:conversation || null
      }
    })
   );
   return response(res,200,"users retrived successfully !!", userWithConversation)
  } catch (error) {
     console.error(error);
    return response(res,500,"Internal server error")
  }
}

